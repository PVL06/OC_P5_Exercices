def sum(a: int, b: int) -> int:
    """
    Return sum of a and b
    Args:
        a: first number
        b: second number
    Returns:
        sum of a and b
    """
    return a + b


def subtraction(a: int, b: int) -> int:
    """
    Return subtract b from a
    Args:
        a: first number
        b: second number to substract at a
    Returns:
        subtract b from a
    """
    return a - b
